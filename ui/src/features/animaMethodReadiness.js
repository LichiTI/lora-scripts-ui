export const ANIMA_METHOD_READINESS = Object.freeze(entries.map((entry) => Object.freeze({ ...entry })));

export function listAnimaMethodReadiness() {
  return ANIMA_METHOD_READINESS;
}

export function getAnimaMethodReadiness(id) {
  return ANIMA_METHOD_READINESS.find((entry) => entry.id === id) || null;
}

export function getVisibleTrainingToggleIds() {
  return ANIMA_METHOD_READINESS
    .filter((entry) => entry.visibleTrainingToggleAllowed)
    .map((entry) => entry.id);
}

export function getGuideOnlyMethodIds() {
  return ANIMA_METHOD_READINESS
    .filter((entry) => !entry.visibleTrainingToggleAllowed && entry.guideEntryKey)
    .map((entry) => entry.id);
}

// Reserves whose real runtime seam is wired (default-off, opt-in only). These are
// surfaced as guide entries describing a usable opt-in path, but must NEVER expose
// a launchable training toggle until an operator signs the runtime-activation gate.
export function getWiredReserveMethodIds() {
  return ANIMA_METHOD_READINESS
    .filter((entry) => entry.reserveSeamWired && !entry.visibleTrainingToggleAllowed)
    .map((entry) => entry.id);
}

export function shouldExposeAsTrainingToggle(id) {
  return Boolean(getAnimaMethodReadiness(id)?.visibleTrainingToggleAllowed);
}
